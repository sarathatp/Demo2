package CollectionsPkg;

public class SwitchClass 
{

	public static void main(String[] args) 
	{
		String n= "Salary";
		
		switch(n)
		{
			case "Job":
				{
					System.out.println("Job");
			
				}
			case "salary":
			{
				System.out.println("Salary");
				break;
			}
			
			case "Vacation":
			{
				
				System.out.println("Vacation");
			}
			
			default:
				System.out.println("no match");
				
		}
			

	}

}
